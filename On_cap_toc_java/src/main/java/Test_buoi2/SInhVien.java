public class SInhVien {
    String ma;
    String ten;
    int tuoi;
    String cnghanh;

    public SInhVien() {
    }

    public SInhVien(String ma, String ten, int tuoi, String cnghanh) {
        this.ma = ma;
        this.ten = ten;
        this.tuoi = tuoi;
        this.cnghanh = cnghanh;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getTuoi() {
        return tuoi;
    }

    public void setTuoi(int tuoi) {
        this.tuoi = tuoi;
    }

    public String getCnghanh() {
        return cnghanh;
    }

    public void setCnghanh(String cnghanh) {
        this.cnghanh = cnghanh;
    }
    
    
}
